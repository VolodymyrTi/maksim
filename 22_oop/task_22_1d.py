topology_example = {
    ("R1", "Eth0/0"): ("SW1", "Eth0/1"),
    ("R2", "Eth0/0"): ("SW1", "Eth0/2"),
    ("R2", "Eth0/1"): ("SW2", "Eth0/11"),
    ("R3", "Eth0/0"): ("SW1", "Eth0/3"),
    ("R3", "Eth0/1"): ("R4", "Eth0/0"),
    ("R3", "Eth0/2"): ("R5", "Eth0/0"),
    ("SW1", "Eth0/1"): ("R1", "Eth0/0"),
    ("SW1", "Eth0/2"): ("R2", "Eth0/0"),
    ("SW1", "Eth0/3"): ("R3", "Eth0/0"),
}
class Topology:
    def __init__(self, topology_dict):
        self.topology = self._normalize(topology_dict)

    def _normalize(self, topology_array):
        tipology = {}
        for key, value in topology_array.items():
            if not tipology.get(value, False):
                tipology[key] = value
        return tipology

    def delete_link(self, key, value):
        if self.topology.get(value, False):
            del self.topology[value]
        elif self.topology.get(key, False):
            del self.topology[key]
        else:
            print("Такого соединения нет")

    def delete_node(self, node):                                                                
        tipology = self.topology.copy() 
        for key, value in self.topology.items():
            if node in key:
                del tipology[key]
            elif node in value:                                     
                del tipology[key]
        if tipology == self.topology:
            print("Такого устройства нет")
        else:
            self.topology = tipology

    def add_link(self, key, value):
        if self.topology.get(key, False):
            if self.topology.get(key, False) == value:
                print ("Такое соединение существует")
            else:
                print ("Cоединение с одним из портов существует")  
        else:    
            self.topology[key] = value
    

